//programme for swapping to varible or number .
public class SwapToNumber {

    public static void main(String args[]) {
        int num1 = 20;
        int num2 = 10;
        int num3;
        System.out.println("Before Swapping A is:" + num1 + " and B is" + num2);
        num3 = num1;
        num1 = num2;
        num2 = num3;
        System.out.println("Affter Swapping A is:" + num1 + " and B is" + num2);
    }
}
